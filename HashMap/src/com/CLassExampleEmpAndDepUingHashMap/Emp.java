package com.CLassExampleEmpAndDepUingHashMap;

public class Emp {
   
	int id;
	String name;
	int did;
	
	
	public Emp(int id, String name, int did) {
		
		this.id = id;
		this.name = name;
		this.did = did;
	}
	@Override
	public String toString() {
		return "Emp [id=" + id + ", name=" + name + ", did=" + did + "]";
	}
	
	
	
	
	
}
