package patternAssignment;

public class Thirdquestion {

	public static void main(String[] args) {
		
		for(int i=1;i<=9;i=i+2)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(" "+j);
			}
			System.out.println();
			
		}
		for(int k=7;k>=1;k=k-2)
		{
			for(int m=1;m<=k;m++)
			{
				System.out.print(" "+m);
			}
			System.out.println();
		}

	}

}
